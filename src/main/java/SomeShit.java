public class SomeShit {
    private String nameOfShit;
    private int quantityOfShit;

    public SomeShit(String nameOfShit, int quantityOfShit) {
        this.nameOfShit = nameOfShit;
        this.quantityOfShit = quantityOfShit;
    }

    public String getNameOfShit() {
        return nameOfShit;
    }

    public void setNameOfShit(String nameOfShit) {
        this.nameOfShit = nameOfShit;
    }

    public int getQuantityOfShit() {
        return quantityOfShit;
    }

    public void setQuantityOfShit(int quantityOfShit) {
        this.quantityOfShit = quantityOfShit;
    }
}
